/*Write a Java program to print all even numbers between 1 to 100. - using while     loop*/
public class example20 {
	public static void main(String[] args) {
	    
	    int i=2;
	    
	    while(i<=100){
	       if(i%2==0){ 
	        System.out.print(i+" ");
	        
	       }
	       i++;
	    }
	}
}	
